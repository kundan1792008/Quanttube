/**
 * Synthesize a single audio description cue via TTS.
 *
 * Production: POSTs to ElevenLabs with a dedicated narrator voice.
 * Stub: writes a placeholder file when TTS_API_KEY is absent.
 */
async function synthesizeDescriptionCue(
  cue: AudioDescriptionCue,
  outputPath: string
): Promise<{ audioPath: string; durationSecs: number }> {
  ensureDir(path.dirname(outputPath));

  if (!AD_TTS_API_KEY) {
    const stubContent = `STUB_AD_AUDIO:cue=${cue.index}:text=${cue.text.substring(0, 30)}`;
    fs.writeFileSync(outputPath, stubContent);
    return { audioPath: outputPath, durationSecs: cue.durationSecs };
  }

  const payload = {
    text: cue.text,
    model_id: "eleven_multilingual_v2",
    voice_settings: {
      stability: 0.7,
      similarity_boost: 0.7,
      style: 0.1,
      use_speaker_boost: false,
      speaking_rate: 1.1, // Slightly faster for time-constrained insertions
    },
  };

  const response = await fetch(`${AD_TTS_API_URL}/${AD_NARRATOR_VOICE_ID}`, {
    method: "POST",
    headers: {
      "xi-api-key": AD_TTS_API_KEY,
      "Content-Type": "application/json",
      Accept: "audio/mpeg",
    },
    body: JSON.stringify(payload),
  });

  if (!response.ok) {
    throw new Error(`TTS API ${response.status}: ${await response.text()}`);
  }

  const audioBuffer = Buffer.from(await response.arrayBuffer());
  fs.writeFileSync(outputPath, audioBuffer);

  // Approximate duration from mp3 bitrate (128 kbps)
  const durationSecs = (audioBuffer.length * 8) / (128 * 1000);

  return { audioPath: outputPath, durationSecs };
}

/**
 * Build an ffmpeg command to mix audio description track with the original
 * video's audio at a specified insertion timestamp.
 *
 * The description audio is ducked in at full volume; original audio is
 * attenuated to 15% during the description for clarity.
 */
export function buildAudioDescriptionMixCommand(
  originalAudioPath: string,
  descriptionAudioPath: string,
  insertAtSecs: number,
  outputPath: string
): string {
  const delay = Math.round(insertAtSecs * 1000); // ms for adelay filter
  return (
    `ffmpeg -i "${originalAudioPath}" -i "${descriptionAudioPath}" ` +
    `-filter_complex ` +
    `"[1:a]adelay=${delay}|${delay}[delayed_desc];` +
    `[0:a][delayed_desc]sidechaincompress=threshold=0.05:ratio=5:attack=200:release=1000[out]" ` +
    `-map "[out]" -c:a aac -b:a 192k -y "${outputPath}"`
  );
}

