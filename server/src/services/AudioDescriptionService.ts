const DESCRIPTION_PROMPT = `You are an expert audio description writer for visually impaired viewers.
Analyze the video frame and describe:
1. Any scene changes or new environments
2. On-screen text, titles, or captions
3. Important character actions and movements
4. Character expressions and emotional state
5. Significant objects or graphics entering the frame

Keep descriptions concise (under 20 words), factual, and in present tense.
Focus on visually significant information that is not conveyed by dialogue.
Output ONLY the description text, nothing else.`;

interface VisionAPIMessage {
  role: "user" | "assistant" | "system";
  content: string | Array<{ type: string; text?: string; image_url?: { url: string; detail: string } }>;
}

interface VisionAPIResponse {
  choices?: Array<{
    message?: { content?: string };
  }>;
}

/**
 * Analyze a single video frame using a vision AI model.
 *
 * Returns the generated description text.
 * Falls back to a deterministic stub description when the API key is absent.
 */
async function analyzeFrame(
  frame: ExtractedFrame,
  context: string
): Promise<string> {
  if (!VISION_API_KEY || !frame.base64Image) {
    // Stub descriptions keyed by frame index for reproducibility
    const stubDescriptions = [
      "A presenter stands at a podium in a conference room.",
      "Text on screen reads: 'Chapter 1: Introduction'.",
      "The scene transitions to an outdoor city street.",
      "A character smiles and gestures towards a whiteboard.",
      "Close-up of hands typing on a laptop keyboard.",
      "Two people sit across from each other at a table.",
      "An animated chart appears showing upward growth.",
      "The camera pans across a mountain landscape.",
      "A notification popup appears in the corner of the screen.",
      "The speaker looks directly at the camera.",
    ];
    const idx = frame.frameIndex % stubDescriptions.length;
    return stubDescriptions[idx]!;
  }

  const messages: VisionAPIMessage[] = [
    {
      role: "system",
      content: DESCRIPTION_PROMPT,
    },
    {
      role: "user",
      content: [
        {
          type: "text",
          text: context
            ? `Previous context: ${context}\n\nDescribe what you see in this frame:`
            : "Describe what you see in this frame:",
        },
        {
          type: "image_url",
          image_url: {
            url: `data:image/jpeg;base64,${frame.base64Image}`,
            detail: "low",
          },
        },
      ],
    },
  ];

  const response = await fetch(VISION_API_URL, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${VISION_API_KEY}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      model: VISION_MODEL,
      messages,
      max_tokens: 100,
      temperature: 0.3,
    }),
  });

  if (!response.ok) {
    const errText = await response.text();
    throw new Error(`Vision API ${response.status}: ${errText}`);
  }

  const data = (await response.json()) as VisionAPIResponse;
  return (data.choices?.[0]?.message?.content ?? "").trim();
}

/**
 * Classify the type of visual event from its description text.
 */
export function classifyVisualEvent(description: string): VisualEventType {
  const lower = description.toLowerCase();

  if (lower.includes("text") || lower.includes("reads:") || lower.includes("caption") ||
      lower.includes("title") || lower.includes("screen") || lower.includes("subtitle")) {
    return "on_screen_text";
  }
  if (lower.includes("smile") || lower.includes("laugh") || lower.includes("frown") ||
      lower.includes("expression") || lower.includes("emotion") || lower.includes("face")) {
    return "character_expression";
  }
  if (lower.includes("scene") || lower.includes("transition") || lower.includes("cut") ||
      lower.includes("exterior") || lower.includes("interior")) {
    return "scene_change";
  }
  if (lower.includes("gesture") || lower.includes("walk") || lower.includes("run") ||
      lower.includes("sits") || lower.includes("stands") || lower.includes("action") ||
      lower.includes("movement") || lower.includes("typing") || lower.includes("holds")) {
    return "character_action";
  }
  if (lower.includes("chart") || lower.includes("graphic") || lower.includes("animation") ||
      lower.includes("icon") || lower.includes("logo") || lower.includes("popup")) {
    return "graphic_element";
  }
  if (lower.includes("room") || lower.includes("outdoor") || lower.includes("street") ||
      lower.includes("office") || lower.includes("landscape") || lower.includes("background")) {
    return "environment_change";
  }
  if (lower.includes("person") || lower.includes("man") || lower.includes("woman") ||
      lower.includes("child") || lower.includes("speaker") || lower.includes("presenter")) {
    return "person_identified";
  }

  return "object_introduced";
}

