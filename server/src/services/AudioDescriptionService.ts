/**
 * Submit an audio description generation job.
 */
export function generateAudioDescription(
  params: GenerateAudioDescriptionParams
): AudioDescriptionJob {
  const {
    videoId,
    videoPath,
    audioPath,
    dialogueSegments = [],
  } = params;

  const jobId = randomId();
  const now = nowIso();

  const job: AudioDescriptionJob = {
    jobId,
    videoId,
    videoPath,
    audioPath,
    status: "queued",
    frameAnalysis: null,
    dialoguePauses: [],
    cues: [],
    combinedAudioPath: null,
    errorMessage: null,
    createdAt: now,
    updatedAt: now,
    completedAt: null,
  };

  audioDescriptionJobs.set(jobId, job);

  runAudioDescriptionJob(jobId, dialogueSegments).catch((err) => {
    logger.error({ jobId, err }, "Audio description job error");
  });

  logger.info(
    { jobId, videoId, videoPath, audioPath },
    "Audio description generation job queued"
  );

  return job;
}

/**
 * Retrieve an audio description job by ID.
 */
export function getAudioDescriptionJob(jobId: string): AudioDescriptionJob | undefined {
  return audioDescriptionJobs.get(jobId);
}

/**
 * List audio description jobs, optionally filtered by videoId.
 */
export function listAudioDescriptionJobs(videoId?: string): AudioDescriptionJob[] {
  const all = Array.from(audioDescriptionJobs.values());
  if (videoId) return all.filter((j) => j.videoId === videoId);
  return all;
}

/**
 * Retrieve the cue timeline for a completed job (used by the player).
 */
export function getAudioDescriptionCues(jobId: string): AudioDescriptionCue[] {
  return audioDescriptionJobs.get(jobId)?.cues ?? [];
}

