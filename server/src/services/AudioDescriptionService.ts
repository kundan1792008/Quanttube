/**
 * Analyze all extracted frames and produce a list of VisualEvents.
 *
 * Deduplicates consecutive similar descriptions and only emits events
 * when the visual content changes meaningfully.
 */
export async function analyzeFrames(
  videoId: string,
  frames: ExtractedFrame[]
): Promise<FrameAnalysisResult> {
  const visualEvents: VisualEvent[] = [];
  let previousDescription = "";
  let analyzedCount = 0;

  for (let i = 0; i < frames.length; i++) {
    const frame = frames[i]!;

    // Check for scene change
    let sceneChangeConfidence = 0;
    if (i > 0) {
      sceneChangeConfidence = detectSceneChange(frames[i - 1]!, frame);
    }

    // Analyze every 5th frame (plus scene changes) to limit API costs
    const shouldAnalyze = i === 0 || i % 5 === 0 || sceneChangeConfidence > 0.4;
    if (!shouldAnalyze) continue;

    const description = await analyzeFrame(frame, previousDescription);
    analyzedCount++;

    // Skip if description is nearly identical to previous (no meaningful change)
    if (description && description !== previousDescription) {
      const eventType = sceneChangeConfidence > 0.4
        ? "scene_change"
        : classifyVisualEvent(description);

      visualEvents.push({
        type: eventType,
        timestamp: frame.timestamp,
        confidence: Math.max(0.7, 1.0 - sceneChangeConfidence * 0.5),
        rawDescription: description,
        frameIndex: frame.frameIndex,
      });

      previousDescription = description;
    }

    // Small delay between API calls to respect rate limits
    if (i < frames.length - 1 && VISION_API_KEY) {
      await sleep(100);
    }
  }

  return {
    videoId,
    frameCount: frames.length,
    analyzedFrameCount: analyzedCount,
    visualEvents,
    analyzedAt: nowIso(),
  };
}

