export interface ExtractedFrame {
  frameIndex: number;
  timestamp: number;
  /** Base64-encoded JPEG image data (empty string in stub mode) */
  base64Image: string;
  /** Width in pixels */
  width: number;
  /** Height in pixels */
  height: number;
}

/**
 * Extract frames from a video file at a given frame rate.
 *
 * In production: uses ffmpeg to extract JPEG frames at ANALYSIS_FRAME_RATE fps.
 * Stub: generates metadata-only frame descriptors with empty image data.
 *
 * The estimated duration is derived from the audioPath size as a very rough
 * proxy when the actual video duration is unavailable.
 */
export async function extractVideoFrames(
  videoPath: string,
  outputDir: string,
  estimatedDurationSecs: number
): Promise<ExtractedFrame[]> {
  ensureDir(outputDir);

  const totalFrames = Math.min(
    Math.ceil(estimatedDurationSecs * ANALYSIS_FRAME_RATE),
    MAX_FRAMES_TO_ANALYZE
  );

  if (!fs.existsSync(videoPath) || !VISION_API_KEY) {
    logger.warn(
      { videoPath },
      "Video file absent or VISION_API_KEY not configured – using stub frames"
    );
    return Array.from({ length: totalFrames }, (_, i) => ({
      frameIndex: i,
      timestamp: i / ANALYSIS_FRAME_RATE,
      base64Image: "",
      width: 1920,
      height: 1080,
    }));
  }

  // Production: invoke ffmpeg to extract frames
  const framePattern = path.join(outputDir, "frame_%05d.jpg");
  const ffmpegCmd = [
    "ffmpeg",
    "-i", videoPath,
    "-vf", `fps=${ANALYSIS_FRAME_RATE}`,
    "-vframes", String(totalFrames),
    "-q:v", "3",
    "-y",
    framePattern,
  ];

  logger.info({ videoPath, totalFrames, framePattern }, "Extracting video frames");

  const { execFile } = await import("child_process");
  const { promisify } = await import("util");
  const execFileAsync = promisify(execFile);

  await execFileAsync(ffmpegCmd[0]!, ffmpegCmd.slice(1));

  const frames: ExtractedFrame[] = [];
  for (let i = 0; i < totalFrames; i++) {
    const framePath = path.join(outputDir, `frame_${String(i + 1).padStart(5, "0")}.jpg`);
    if (fs.existsSync(framePath)) {
      const imageData = fs.readFileSync(framePath);
      frames.push({
        frameIndex: i,
        timestamp: i / ANALYSIS_FRAME_RATE,
        base64Image: imageData.toString("base64"),
        width: 1920,
        height: 1080,
      });
    }
  }

  return frames;
}

