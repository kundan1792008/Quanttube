/**
 * Merge visual events that are temporally close and thematically related
 * into a single combined description suitable for audio output.
 */
export function composeDescriptionText(events: VisualEvent[]): string {
  if (events.length === 0) return "";
  if (events.length === 1) return events[0]!.rawDescription;

  // For multiple events, combine into a flowing sentence
  const parts = events.map((e) => e.rawDescription.replace(/\.$/, ""));
  if (parts.length === 2) {
    return `${parts[0]}, then ${parts[1]}.`;
  }
  const last = parts.pop()!;
  return `${parts.join("; ")}, and ${last}.`;
}

/**
 * Assign visual events to the best available dialogue pauses.
 *
 * Each visual event is placed in the nearest subsequent pause that has
 * sufficient available duration.
 */
export function assignEventsToPauses(
  events: VisualEvent[],
  pauses: DialoguePause[],
  estimatedWordsPerSecond = 2.5
): AudioDescriptionCue[] {
  const cues: AudioDescriptionCue[] = [];
  const pauseUsage = new Map<number, number>(); // pause index → seconds used

  for (const event of events) {
    // Find the best pause: next available pause after (or at) the event timestamp
    let bestPauseIdx = -1;
    let bestDelay = Infinity;

    for (let i = 0; i < pauses.length; i++) {
      const pause = pauses[i]!;
      if (pause.start < event.timestamp) continue; // pause is before the event

      const used = pauseUsage.get(i) ?? 0;
      const available = pause.duration * MAX_PAUSE_USAGE_RATIO - used;
      if (available <= 0) continue; // pause is fully used

      const delay = pause.start - event.timestamp;
      if (delay < bestDelay) {
        bestDelay = delay;
        bestPauseIdx = i;
      }
    }

    if (bestPauseIdx === -1) continue; // No suitable pause found

    const pause = pauses[bestPauseIdx]!;
    const text = event.rawDescription;
    const wordCount = text.split(/\s+/).filter(Boolean).length;
    const estimatedDuration = Math.ceil((wordCount / estimatedWordsPerSecond) * 10) / 10;

    const used = pauseUsage.get(bestPauseIdx) ?? 0;
    const available = pause.duration * MAX_PAUSE_USAGE_RATIO - used;
    if (estimatedDuration > available) continue;

    const insertAt = pause.start + used + 0.2; // 200ms buffer
    pauseUsage.set(bestPauseIdx, used + estimatedDuration + 0.3);

    cues.push({
      index: cues.length,
      insertAt,
      durationSecs: estimatedDuration,
      text,
      visualEvents: [event],
      audioPath: null,
      audioGenerated: false,
    });
  }

  return cues.sort((a, b) => a.insertAt - b.insertAt);
}

