/**
 * Compare two base64 frames and return a scene-change confidence score.
 *
 * Production: uses perceptual hashing (pHash) or a CNN-based change detector.
 * Stub: returns a deterministic pseudo-score based on frame index delta.
 */
export function detectSceneChange(
  frameA: ExtractedFrame,
  frameB: ExtractedFrame
): number {
  if (!frameA.base64Image || !frameB.base64Image) {
    // Stub: generate pseudo-random scene change probability
    const timeDelta = Math.abs(frameB.timestamp - frameA.timestamp);
    // Scene changes are more likely after longer intervals
    const baseProb = Math.min(0.15 * timeDelta, 0.5);
    // Use frame indices as a pseudo-random seed
    const seed = (frameA.frameIndex * 7919 + frameB.frameIndex * 6271) % 1000;
    return baseProb + (seed / 1000) * 0.3;
  }

  // Rough pixel similarity estimate via sample comparison
  const aLen = frameA.base64Image.length;
  const bLen = frameB.base64Image.length;
  const sizeDiff = Math.abs(aLen - bLen) / Math.max(aLen, bLen);
  return Math.min(sizeDiff * 3, 1.0);
}

