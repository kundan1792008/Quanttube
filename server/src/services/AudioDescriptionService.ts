/**
 * Detect natural pauses in dialogue where audio descriptions can be inserted.
 *
 * When dialogue segments are provided, pauses are the gaps between them.
 * Otherwise, a set of evenly-spaced synthetic pauses is returned.
 */
export function detectDialoguePauses(
  dialogueSegments: Array<{ start: number; end: number; text: string }>,
  videoDurationSecs: number
): DialoguePause[] {
  const pauses: DialoguePause[] = [];

  if (dialogueSegments.length === 0) {
    // No dialogue data: create a pause every 30 seconds
    const interval = 30;
    for (let t = 5; t < videoDurationSecs - 5; t += interval) {
      pauses.push({
        start: t,
        end: t + MIN_PAUSE_DURATION_SECS * 2,
        duration: MIN_PAUSE_DURATION_SECS * 2,
        assigned: false,
      });
    }
    return pauses;
  }

  // Sort segments by start time
  const sorted = [...dialogueSegments].sort((a, b) => a.start - b.start);

  // Gap before first segment
  if (sorted[0]!.start > MIN_PAUSE_DURATION_SECS) {
    pauses.push({
      start: 0,
      end: sorted[0]!.start,
      duration: sorted[0]!.start,
      assigned: false,
    });
  }

  // Gaps between consecutive segments
  for (let i = 0; i < sorted.length - 1; i++) {
    const gapStart = sorted[i]!.end;
    const gapEnd = sorted[i + 1]!.start;
    const gapDuration = gapEnd - gapStart;

    if (gapDuration >= MIN_PAUSE_DURATION_SECS) {
      pauses.push({
        start: gapStart,
        end: gapEnd,
        duration: gapDuration,
        assigned: false,
      });
    }
  }

  // Gap after last segment
  const lastEnd = sorted[sorted.length - 1]!.end;
  if (videoDurationSecs - lastEnd > MIN_PAUSE_DURATION_SECS) {
    pauses.push({
      start: lastEnd,
      end: videoDurationSecs,
      duration: videoDurationSecs - lastEnd,
      assigned: false,
    });
  }

  return pauses;
}

