async function runAudioDescriptionJob(
  jobId: string,
  dialogueSegments: Array<{ start: number; end: number; text: string }>
): Promise<void> {
  const job = audioDescriptionJobs.get(jobId);
  if (!job) return;

  try {
    const outputDir = safeOutputPath(AD_OUTPUT_ROOT, job.videoId, job.jobId);
    ensureDir(outputDir);

    // Estimate video duration from audio file size (very rough; 128 kbps mp3)
    let estimatedDurationSecs = 120; // default fallback
    if (fs.existsSync(job.audioPath)) {
      const stat = fs.statSync(job.audioPath);
      estimatedDurationSecs = Math.max(10, (stat.size * 8) / (128 * 1000));
    } else if (dialogueSegments.length > 0) {
      const maxEnd = Math.max(...dialogueSegments.map((s) => s.end));
      estimatedDurationSecs = maxEnd + 10;
    }

    // Step 1: Extract video frames
    job.status = "extracting_frames";
    job.updatedAt = nowIso();

    const framesDir = path.join(outputDir, "frames");
    const frames = await extractVideoFrames(
      job.videoPath,
      framesDir,
      estimatedDurationSecs
    );

    // Step 2: Analyze frames
    job.status = "analyzing_frames";
    job.updatedAt = nowIso();

    const frameAnalysis = await analyzeFrames(job.videoId, frames);
    job.frameAnalysis = frameAnalysis;

    // Step 3: Detect dialogue pauses
    job.status = "detecting_pauses";
    job.updatedAt = nowIso();

    const pauses = detectDialoguePauses(dialogueSegments, estimatedDurationSecs);
    job.dialoguePauses = pauses;

    // Step 4: Generate description cues
    job.status = "generating_descriptions";
    job.updatedAt = nowIso();

    const cues = assignEventsToPauses(frameAnalysis.visualEvents, pauses);
    job.cues = cues;

    // Step 5: Synthesize audio for each cue
    job.status = "synthesizing_audio";
    job.updatedAt = nowIso();

    for (let i = 0; i < cues.length; i++) {
      const cue = cues[i]!;
      const cueAudioPath = path.join(outputDir, `cue_${String(i).padStart(4, "0")}.mp3`);

      try {
        const { audioPath: synthesizedPath, durationSecs } = await synthesizeDescriptionCue(
          cue,
          cueAudioPath
        );
        cue.audioPath = synthesizedPath;
        cue.durationSecs = durationSecs;
        cue.audioGenerated = !!AD_TTS_API_KEY;
      } catch (cueErr) {
        const cueErrMsg = cueErr instanceof Error ? cueErr.message : String(cueErr);
        logger.warn({ jobId, cueIndex: i, err: cueErrMsg }, "Failed to synthesize description cue");
      }
    }

    // Build combined audio track path (actual mixing requires ffmpeg in production)
    const combinedPath = path.join(outputDir, "audio_descriptions_combined.mp3");
    if (!AD_TTS_API_KEY) {
      fs.writeFileSync(
        combinedPath,
        `STUB_COMBINED_AD:${sanitizePathComponent(job.videoId)}:cues=${cues.length}`
      );
    }
    job.combinedAudioPath = combinedPath;

    job.status = "completed";
    job.completedAt = nowIso();
    job.updatedAt = nowIso();

    logger.info(
      {
        jobId,
        videoId: job.videoId,
        visualEvents: frameAnalysis.visualEvents.length,
        pauses: pauses.length,
        cues: cues.length,
      },
      "Audio description generation completed"
    );
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    job.status = "failed";
    job.errorMessage = msg;
    job.updatedAt = nowIso();
    logger.error({ jobId, err: msg }, "Audio description generation failed");
  }
}

